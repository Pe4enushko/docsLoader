CREATE TABLE IF NOT EXISTS public."MedKard" (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  visit_guid_1c text NOT NULL,
  score_visit_identification integer NOT NULL,
  score_anamnes integer NOT NULL,
  score_inspection integer NOT NULL,
  score_dynamic integer NOT NULL,
  score_diagnosis integer NOT NULL,
  score_recommendations integer NOT NULL,
  score_structure integer NOT NULL,
  issues text NULL,
  summary text NULL,
  score_overall integer NOT NULL,
  risk_level text NOT NULL,
  "Inspection_data" jsonb NULL,
  diagnosis_data jsonb NULL,
  services_data jsonb NULL,
  visit_date date NULL,
  human_readable text NULL,
  patient text NULL,
  CONSTRAINT "MedKard_pkey" PRIMARY KEY (id),
  CONSTRAINT "MedKard_visit_guid_1c_key" UNIQUE (visit_guid_1c)
) TABLESPACE pg_default;
